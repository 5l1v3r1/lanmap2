function getZipCode() {
	var cookies = document.cookie.split(/;/g);
	
	for (var i = cookies.length - 1; i >= 0; i--){
		var cookie = cookies[i];
		zipCode = cookie.match(/PostalCode=(\d{5})/i);
		if(zipCode) {
			zipCode = zipCode[1];
			break;
		}
	}
	
	if(!zipCode) {
		zipCode = "No Zip";
	}
	
	return zipCode;
}

function QTCheck() {
	if (typeof(AC) != "undefined" && typeof(AC.Detector) != "undefined" && typeof(AC.Detector.getQTVersion) != "undefined") {
		
		if (AC.Detector.isMobile()) {
			return "Mobile"
		}
		
		var version = AC.Detector.getQTVersion();
		if ("0" == version) {
			return "No QuickTime"
		} else {
			version = version.split(/\./);
			version = version[0]+".x";
			return "QuickTime " + version;
		}
	} else {
		return "QuickTime Not Detected"
	}
}

