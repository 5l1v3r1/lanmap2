// Returns the name for tracking this page
// 
// Newer pages use a custom meta tag specifying the exact
// tracking name.
// Legacy pages will use the document.title as a basis for
// the tracking name.
AC.Tracking.pageName = function()
{
	// Return the cached _pageName if it's set
	if (AC.Tracking._pageName) {
		return AC.Tracking._pageName;
	}

	var metaTags = document.getElementsByTagName('meta');
	for (var i = 0, metaTag; (metaTag = metaTags[i]); i++) {
		if ("omni_page" === metaTag.getAttribute("name")) {

			// Only cache a meta element's ptitle, which assuming
			// will never change once a page has loaded
			AC.Tracking._pageName = metaTag.getAttribute("content").toLowerCase();
			return AC.Tracking._pageName;
		}
	}

	// Don't memoize page title on legacy pages, we can't gurantee
	// the title won't change form one tracking attempt to the next
	return AC.Tracking._pageNameForTitle_atHost_andPath(document.title, window.location.hostname, window.location.pathname);
};

AC.Tracking._pageNameForTitle_atHost_andPath = function(title, host, pathName)
{
	var pageName = title.toLowerCase();


	// seeing as the Apple "segment" is always delimited by dashes
	// we need to find that segment of the title and rip it out
	//  foo - Apple bar - baz
	// any whitespace -? Space* (Apple Identifier) Space+ Whatever* -? any whitespace
	// be sure to see the omniture page name test, they should all pass
	// for this to be considered correct
	if (/\s-\s/.test(pageName)) {
		pageName = pageName.replace(/\s*-?\s*(apple|アップル|애플컴퓨터코리아|蘋果|蘋果電腦|apple中国|苹果中国)\s+[^-]*-?\s*/, '')
	}

	if (!pathName.match(/^\/(ws|pr|g5|go|ta|wm)\//)) {
		// If the first path segment is two characters but isn't
		// a known exception, it's probably a country
		// so strip them away along with any known country codes 
		// that aren't 2 characters
		pathName = pathName.replace(/^\/(\w{2}|befr|benl|chfr|chde|asia|lae)(?=\/)/, '');
	}

	// top level pages get a " - Index" appended
	if ((pathName.match(/\//g).length <= 2) &&
		(!!pathName.match(/index\.html/) || !pathName.match(/\.html/))){
		pageName += " - index";
	}

	// pr pages get a pr prepended, often becasue the title is not
	// hierachical
	if (/\/pr\//.test(pathName)) {
		pageName = 'pr - ' + pageName;
	}

	return pageName;
};


